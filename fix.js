const fs = require('fs');
const lines = [];
lines.push("'use client'");
lines.push("");
lines.push("import { supabase } from '@/lib/supabase'");
lines.push("import { useEffect, useState } from 'react'");
lines.push("");
lines.push("interface NavbarProps {");
lines.push("  activePage?: string");
lines.push("  userRole?: string");
lines.push("}");
lines.push("");
lines.push("export default function Navbar({ activePage, userRole }: NavbarProps) {");
lines.push("  const [userName, setUserName] = useState('')");
lines.push("");
lines.push("  useEffect(() => {");
lines.push("    async function getName() {");
lines.push("      const { data: { session } } = await supabase.auth.getSession()");
lines.push("      if (!session) return");
lines.push("      const { data: profile } = await supabase");
lines.push("        .from('profiles')");
lines.push("        .select('full_name')");
lines.push("        .eq('id', session.user.id)");
lines.push("        .single()");
lines.push("      if (profile) setUserName(profile.full_name || '')");
lines.push("    }");
lines.push("    getName()");
lines.push("  }, [])");
lines.push("");
lines.push("  async function handleLogout() {");
lines.push("    await supabase.auth.signOut()");
lines.push("    window.location.href = '/login'");
lines.push("  }");
lines.push("");
lines.push("  const navItems = [");
lines.push("    { label: 'Planner', href: '/dashboard' },");
lines.push("    { label: 'My Bookings', href: '/my-bookings' },");
lines.push("    { label: 'New Booking', href: '/new-booking' },");
lines.push("    { label: 'Calendar Sync', href: '/calendar-sync' },");
lines.push("    ...(userRole === 'admin' ? [");
lines.push("      { label: 'Admin', href: '/admin' },");
lines.push("      { label: 'Stats', href: '/stats' },");
lines.push("    ] : []),");
lines.push("  ]");
lines.push("");
lines.push("  return (");
lines.push("    <nav className=\"bg-gray-900 text-white px-4 py-3 flex items-center justify-between\">");
lines.push("      <div className=\"flex items-center gap-6\">");
lines.push("        <a href=\"/dashboard\" className=\"flex items-center gap-2 mr-2\">");
lines.push("          <img src=\"/crest.png\" alt=\"Crest\" className=\"w-8 h-8 object-contain\" />");
lines.push("          <div className=\"leading-tight\">");
lines.push("            <div className=\"font-bold text-sm\">St. Saviours</div>");
lines.push("            <div className=\"text-gray-400 text-xs\">GAA and LGFA</div>");
lines.push("          </div>");
lines.push("        </a>");
lines.push("        <div className=\"hidden md:flex items-center gap-1\">");
lines.push("          {navItems.map((item) => (");
lines.push("            <a");
lines.push("              key={item.href}");
lines.push("              href={item.href}");
lines.push("              className={");
lines.push("                activePage === item.label");
lines.push("                  ? 'px-3 py-2 rounded-lg text-sm font-medium bg-gray-700 text-white'");
lines.push("                  : 'px-3 py-2 rounded-lg text-sm font-medium text-gray-300 hover:text-white hover:bg-gray-800'");
lines.push("              }");
lines.push("            >");
lines.push("              {item.label}");
lines.push("            </a>");
lines.push("          ))}");
lines.push("        </div>");
lines.push("      </div>");
lines.push("      <div className=\"flex items-center gap-3\">");
lines.push("        <span className=\"text-sm text-gray-300 hidden md:block\">{userName}</span>");
lines.push("        <button");
lines.push("          onClick={handleLogout}");
lines.push("          className=\"bg-white text-gray-900 px-4 py-2 rounded-lg text-sm font-semibold hover:bg-gray-200 transition-colors\"");
lines.push("        >");
lines.push("          Sign out");
lines.push("        </button>");
lines.push("      </div>");
lines.push("    </nav>");
lines.push("  )");
lines.push("}");
fs.writeFileSync('components/Navbar.tsx', lines.join('\n'), 'utf8');
console.log('Done - file written');
